<template>
    <section class="py-16 bg-white overflow-hidden">
        <div class="container mx-auto px-4">
            <div class="text-center mb-12">
                <h2 class="text-3xl md:text-4xl font-bold text-gray-800 mb-4">Our Services</h2>
                <p class="text-gray-600 max-w-2xl mx-auto">Comprehensive real estate solutions for all your property needs in Bangladesh</p>
            </div>

            <!-- Carousel Container -->
            <div class="relative">
                <!-- Services Slider -->
                <div class="services-slider">
                    <div class="services-track" :style="{ transform: `translateX(-${currentSlide * slideWidth}%)` }">
                        <!-- Service Cards -->
                        <div v-for="(service, index) in services"
                             :key="index"
                             class="service-card">
                            <div class="group bg-white rounded-2xl p-8 shadow-lg hover:shadow-2xl transition-all duration-300 border border-gray-100"
                                 :class="`hover:border-${service.borderColor}-500`">
                                <div class="w-16 h-16 rounded-xl flex items-center justify-center mb-6 group-hover:scale-110 transition-transform"
                                     :style="`background: linear-gradient(to bottom right, ${service.colorFrom}, ${service.colorTo})`">
                                    <i :class="service.icon" class="text-white text-2xl"></i>
                                </div>
                                <h3 class="text-xl font-bold text-gray-800 mb-3">{{ service.title }}</h3>
                                <p class="text-gray-600 leading-relaxed">{{ service.description }}</p>
                            </div>
                        </div>
                    </div>
                </div>

                <!-- Navigation Dots -->
                <div class="flex justify-center gap-2 mt-8">
                    <button v-for="(dot, index) in totalSlides"
                            :key="index"
                            @click="goToSlide(index)"
                            class="w-3 h-3 rounded-full transition-all duration-300"
                            :class="currentSlide === index ? 'bg-blue-600 w-8' : 'bg-gray-300 hover:bg-gray-400'">
                    </button>
                </div>

                <!-- Pause/Play Button -->
                <div class="text-center mt-4">
                    <button @click="toggleAutoPlay"
                            class="text-gray-600 hover:text-blue-600 transition-colors">
                        <i :class="isPlaying ? 'fas fa-pause' : 'fas fa-play'"></i>
                        {{ isPlaying ? 'Pause' : 'Play' }}
                    </button>
                </div>
            </div>
        </div>
    </section>
</template>

<script>
export default {
    name: 'ServicesSection',
    data() {
        return {
            currentSlide: 0,
            isPlaying: true,
            autoPlayInterval: null,
            services: [
                {
                    icon: 'fas fa-map-marked-alt',
                    title: 'Land Development',
                    description: 'Professional land surveying, planning, and development services across Bangladesh',
                    colorFrom: '#10b981',
                    colorTo: '#059669',
                    borderColor: 'green'
                },
                {
                    icon: 'fas fa-home',
                    title: 'Property Sales',
                    description: 'Buy and sell residential, commercial, and agricultural properties with expert guidance',
                    colorFrom: '#3b82f6',
                    colorTo: '#2563eb',
                    borderColor: 'blue'
                },
                {
                    icon: 'fas fa-handshake',
                    title: 'Real Estate Consultancy',
                    description: 'Expert advice on investment opportunities, market trends, and property valuation',
                    colorFrom: '#8b5cf6',
                    colorTo: '#7c3aed',
                    borderColor: 'purple'
                },
                {
                    icon: 'fas fa-building',
                    title: 'Property Management',
                    description: 'Complete property maintenance, tenant management, and rental services',
                    colorFrom: '#f59e0b',
                    colorTo: '#d97706',
                    borderColor: 'orange'
                },
                {
                    icon: 'fas fa-receipt',
                    title: 'Plot Booking',
                    description: 'Easy installment plans and booking options for residential and commercial plots',
                    colorFrom: '#14b8a6',
                    colorTo: '#0d9488',
                    borderColor: 'teal'
                },
                {
                    icon: 'fas fa-file-contract',
                    title: 'Legal Documentation',
                    description: 'Complete support for land registration, mutation, and legal paperwork',
                    colorFrom: '#ef4444',
                    colorTo: '#dc2626',
                    borderColor: 'red'
                },
                {
                    icon: 'fas fa-hard-hat',
                    title: 'Construction Services',
                    description: 'End-to-end construction solutions from planning to completion',
                    colorFrom: '#6366f1',
                    colorTo: '#4f46e5',
                    borderColor: 'indigo'
                },
                {
                    icon: 'fas fa-chart-line',
                    title: 'Investment Advisory',
                    description: 'Strategic guidance for profitable real estate investments in prime locations',
                    colorFrom: '#eab308',
                    colorTo: '#ca8a04',
                    borderColor: 'yellow'
                }
            ]
        }
    },
    computed: {
        totalSlides() {
            // Desktop: 4 cards per slide, Mobile: 1 card
            const cardsPerSlide = window.innerWidth >= 1024 ? 4 : 1;
            return Math.ceil(this.services.length / cardsPerSlide);
        },
        slideWidth() {
            return window.innerWidth >= 1024 ? 100 : 100;
        }
    },
    mounted() {
        this.startAutoPlay();
    },
    beforeDestroy() {
        this.stopAutoPlay();
    },
    methods: {
        startAutoPlay() {
            this.autoPlayInterval = setInterval(() => {
                this.nextSlide();
            }, 3000); // 3 seconds
        },
        stopAutoPlay() {
            if (this.autoPlayInterval) {
                clearInterval(this.autoPlayInterval);
            }
        },
        nextSlide() {
            this.currentSlide = (this.currentSlide + 1) % this.totalSlides;
        },
        goToSlide(index) {
            this.currentSlide = index;
        },
        toggleAutoPlay() {
            this.isPlaying = !this.isPlaying;
            if (this.isPlaying) {
                this.startAutoPlay();
            } else {
                this.stopAutoPlay();
            }
        }
    }
}
</script>

