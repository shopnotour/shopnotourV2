    public function index(Request $request)
    {
        $reissueParams = session('reissue_search_params');

        if ($reissueParams) {
            $validated = $reissueParams;
            session()->forget('reissue_search_params');
            Log::info('🔄 Reissue Flight Search', [
                'reissue_id' => $validated['reissue_id'] ?? null,
                'booking_id' => $validated['booking_id'] ?? null
            ]);
        } else {
            $validated = $request->validate([
                'trip_type'       => 'required|in:oneway,round,multi',
                'segments'        => 'required|array|min:1',
                'segments.*.from' => 'required',
                'segments.*.to'   => 'required',
                'segments.*.departure' => 'required|date',
                'return_date'     => 'nullable|date',
                'adults'          => 'required|integer|min:1|max:9',
                'children'        => 'nullable|integer|min:0',
                'infants'         => 'nullable|integer|min:0',
                'children_ages'   => 'nullable|array',
                'children_ages.*' => 'integer|min:0|max:12',
                'travel_class'    => 'required|in:ECONOMY,BUSINESS,FIRST',
                'airline_codes'   => 'nullable',
            ]);

            $passengerErrors = $this->passengerProcessor->validatePassengers($validated);
            if (!empty($passengerErrors)) {
                if ($request->expectsJson() || $request->ajax()) {
                    return response()->json(['errors' => $passengerErrors], 422);
                }
                return redirect()->back()->withErrors($passengerErrors)->withInput();
            }

            $tripValidationErrors = $this->segmentProcessor->validateRoundTrip(
                $validated['trip_type'],
                $validated['return_date'] ?? '',
                $validated['segments']
            );
            if (!empty($tripValidationErrors)) {
                if ($request->expectsJson() || $request->ajax()) {
                    return response()->json(['errors' => $tripValidationErrors], 422);
                }
                return redirect()->back()->withErrors($tripValidationErrors)->withInput();
            }
        }

        // ✅ Streaming response
        if ($request->expectsJson() || $request->ajax()) {
            session(['flight_search_params' => $validated]);
            return response()->stream(function () use ($validated) {
                

                $this->flightSearch->searchStream($validated, function ($flight) {
                    echo "data: " . json_encode($flight) . "\n\n";
                    ob_flush();
                    flush();
                });

                echo "data: [DONE]\n\n";
                ob_flush();
                flush();

            }, 200, [
                'Content-Type'      => 'text/event-stream',
                'Cache-Control'     => 'no-cache',
                'X-Accel-Buffering' => 'no',
            ]);
        }

        // ✅ Normal page load — frontend streaming করবে
        session(['flight_search_params' => $validated]);

        return view('Flight::frontend.vueSearch.flight_search', [
            'flights'      => [],
            'searchParams' => $validated,
        ]);
    }

    public function getData(Request $request, $id)
    {
        $row = $this->flightClass::with(['flightSeat.seatType', 'airportFrom', 'airportTo', 'airline', 'bookingPassengers'])->find($id);
        if (empty($row)) {
            return $this->sendError('no found');
        } else {
            if (!empty($row->airline)) {
                $row->airline->append(['image_url']);
            }
            $bookingPassengers = $row->bookingPassengers->countBy('seat_type')->toArray();
            if (!empty($row->flightSeat)) {
                foreach ($row->flightSeat as &$value) {
                    if (!empty($bookingPassengers[$value->seat_type])) {
                        $value->max_passengers = $value->max_passengers - $bookingPassengers[$value->seat_type];
                        if ($value->max_passengers < 0) {
                            $value->max_passengers = 0;
                        }
                    }
                    $value->price_html = format_money($value->price);
                    $value->number = 0;
                }
            }
            $row->departure_time_html = $row->departure_time->format('H:i');
            $row->departure_date_html = $row->departure_time->format('D, d M y');
            $row->arrival_time_html = $row->arrival_time->format('H:i');
            $row->arrival_date_html = $row->arrival_time->format('D, d M y');

            return $this->sendSuccess(['data' => $row->toArray()], 'founded');
        }
    }

    /**
     * Helper method to get airline image URL
     */
    private function getAirlineImage($airlineCode)
    {
        try {
            $airline = \Modules\Flight\Models\Airline::where('designator', $airlineCode)->first();
            if ($airline && $airline->image_id) {
                return get_file_url($airline->image_id, 'thumb', true);
            }
        } catch (\Exception $e) {
            // Log error if needed
        }
        return ''; // Return empty string if no image found
    }

    /**
     * Get all airports for multi-city segments
     */
    private function getSegmentAirports(Request $request)
    {
        $segments = $request->input('segments', []);
        $segmentAirports = [];

        foreach ($segments as $index => $segment) {
            $fromId = $segment['from'] ?? null;
            $toId = $segment['to'] ?? null;

            $segmentAirports[$index] = [
                'from' => $fromId ? Airport::find($fromId) : null,
                'to' => $toId ? Airport::find($toId) : null,
            ];
        }

        return $segmentAirports;
    }

