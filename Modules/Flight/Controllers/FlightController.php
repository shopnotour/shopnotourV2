    /**
     * Check if time matches given time range
     */
    private function matchesTimeRange($timeString, $range)
    {
        if (empty($timeString)) return false;

        try {
            $time = \Carbon\Carbon::parse($timeString);
            $hour = $time->hour;

            // Parse range (e.g., "06-12")
            if (is_string($range)) {
                $range = [$range];
            }

            foreach ($range as $r) {
                [$start, $end] = explode('-', $r);
                $start = (int)$start;
                $end = (int)$end;

                if ($end === 0) $end = 24; // Handle 18-00

                if ($start < $end) {
                    // Normal range
                    if ($hour >= $start && $hour < $end) return true;
                } else {
                    // Overnight range
                    if ($hour >= $start || $hour < $end) return true;
                }
            }
        } catch (\Exception $e) {
            return false;
        }

        return false;
    }

    /**
     * Parse ISO 8601 duration to minutes
     */
    private function parseDurationToMinutes($duration)
    {
        if (empty($duration)) return 0;

        // Handle ISO 8601 format: PT2H30M
        if (preg_match('/PT(?:(\d+)H)?(?:(\d+)M)?/', $duration, $matches)) {
            $hours = isset($matches[1]) ? (int)$matches[1] : 0;
            $minutes = isset($matches[2]) ? (int)$matches[2] : 0;
            return ($hours * 60) + $minutes;
        }

        return 0;
    }

